	<h1>INSCRIPTIONS</h1>
	<section>
		<p>L’inscription à la formation continue</p>

		<p>L’inscription en première année (semestre I et II) pour la formation initiale est conditionnée à la réussite au concours organisé par l’IAI-Togo.</p>

		<p>Les cours dans cette classe constituent une formation de base en informatique, mathématique et gestion. Les cours d’informatique portent sur la programmation et l’utilisation des environnements systèmes.</p>

		<p>Les  étudiants ayant réussies les deux semestres sont autorisés à s’inscrire en deuxième année (semestre III et IV). sont également autorisé à s’inscrire les  titulaire de DUT en Informatique.</p>

		<p>Une formation approfondie en programmation est donnée aux étudiants. Elle est complétée par une introduction aux techniques de conception des systèmes d’information. Un complément en mathématiques appliquées est aussi donné. Enfin, des cours de gestion sont introduits pour préparer l’étudiant à découvrir la vie des entreprises dans lesquelles il assurera sa future carrière. L’année se termine par un stage en entreprises de quatre mois, confrontant l’étudiant au milieu professionnel. Ce stage a permis d’établir la renommée de l’IAI à cause du caractère original de la validation sur le site. Le stage se déroule selon les modalités décrites dans le cahier de charge du stage de programmation ou de maintenance. Il intervient pour un quart dans le total des coefficients pour le passage en troisième année ou l’obtention du diplôme de programmeur.</p>

		<p>L’inscription en troisième année (semestre V et VI) est subordonnée par la réussite en semestre III et IV</p>

		<p>Cette année est essentiellement consacrée à l’étude des techniques de réalisation de système d’information (analyse, bases de données). Durant le dernier semestre en même temps que les cours continuent, les étudiants sont amenés à réaliser un projet de fin de cycle. Les étudiants ont la charge de mener l’étude et la réalisation d’un outil apportant une solution au problème. Ce projet est l’un des piliers qui fait l’expérience et le côté opérationnel des analystes programmeurs, et confère à l’IAI sa notoriété.</p>
