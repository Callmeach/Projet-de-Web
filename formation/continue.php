	<h1>FORMATION CONTINUE</h1>
	<p>L’offre de formation continue de l’Institut Africain d’Informatique, Représentation du Togo concerne tous les publics sans condition d’âge ou de niveau d’études : salariés, travailleurs indépendants, professions libérales, étudiants, demandeurs d’emploi, retraités, particuliers.</p>
	<p></p>
	<h2>Génie Logiciel</h2>
	<p></p>
	<p>La filière Génie Logiciel forme des informaticiens capables de concevoir et de maintenir des logiciels en s’appuyant sur des méthodes et des outils très évolués. les principaux acquis après une formation en Génie Logiciel sont la maîtrise des systèmes d’information, des outils d’analyse et de modélisation, de programmation dans les langages de pointe ainsi que l’administration des bases de données. A cet effet, l’IAI-TOGO met à la disposition de ses étudiants des centres de calcul équipés de micro-ordinateurs le tout dans un réseau local pour le partage des ressources. La formation est théorique et pratique renforcée par des stages d’entreprise.</p>
	<p></p>
	<h2>Système et Réseaux</h2>
	<p></p>
	<p>La filière Systèmes et Réseaux, créée il y a deux ans forme des informaticiens capables de concevoir, implanter, interconnecter et administrer des réseaux informatiques et d’assurer également la maintenance de tout matériel informatique. L’accent est donc mis sur l’étude des réseaux sous la norme CISCO CCNA, les systèmes d’exploitation, l’électricité, l’électronique et la maintenance.</p>
	<p></p>
	<p>A cet effet, l’IAI-TOGO met à la disposition de ses étudiants des salles de réseaux équipées de micro-ordinateurs le tout dans un réseau local pour le partage des ressources.</p>
	<p></p>
	<p>La formation est très pratique. La première promotion de sept (7) étudiants ont reçu leur diplôme en 2010.</p>
	<p></p>
	<h2>Multimédia</h2>
	<p></p>
	<p>La licence Multimédia, Technologie Web et Infographie (M-TWI), vise à apporter aux étudiants les compétences nécessaires pour réussir leur projet, qu’il soit de création, d’administration ou d’industrialisation des produits de communication audiovisuelle.</p>
	<p></p>
	<p>L’identité de la formation est liée à la nature pluridisciplinaire des enseignements (connaissance technologiques, informatique, gestion de projet, création audiovisuelle, infographie) et à l’opportunité d’affirmer une bonne communication via des documents multimédias. Il s’agit aussi d’intégrer une culture de management de projets multimédia et infographie.</p>
	<p></p>
	<p>La formation repose sur un enseignement conjoint (50% de l’IAI-TOGO, 50% de l’UTBM). Chaque unité d’enseignement assurée par l’UTBM est délivrée majoritairement à distance avec un accompagnement en présentiel sur une semaine. Le Centre d’Innovation Pédagogique (CIP) de l’UTBM met à disposition sa plate-forme d’enseignement et de soutien à distance (cours, devoirs, projets, conférences vidéos, forums, etc).</p>
	<p></p>
	<p>Le rythme d’enseignement donne une possibilité de suivre la formation en alternance ou de suivre des modules pour une certification dans un domaine ciblé.</p>
