	<h1>FORRMATION CERTIFIANTE</h1>
	<p>A côté des diplômes bien connus de tous, il existe des formations très opérationnelles et généralement plus courtes qui débouchent sur un certificat ou un titre professionnel.</p>
	<p>Mais qu’est ce qu’une formation certifiante ? Souvent moins chères que les formations diplômantes, les formations certifiantes sont appréciées des recruteurs et très valorisées sur un CV car leur enseignement est en lien direct avec les besoins des entreprises d’une branche donnée. C’est là leur force, mais aussi leur faiblesse car de ce fait elles restent dédiées à un métier précis. La formation certifiante n’est donc pas adapté à quelqu’un qui cherche des compétences transversales lui permettant d’évoluer dans plusieurs secteurs d’activité.</p>
	<p>L’Académie CISCO Locale de l’IAI-TOGO offre trois curricula :</p>
	<p></p>
	<ul>
		<li>IT Essentials,</li>
		<li>CCNA ( Il se décompose en trois (3) modules: CCNA1, CCNA2 et CCNA3)</li>
		<li>CCNA Security.</li>
	</ul>
	<p>Tous ces cours sont accessibles via Internet. Chaque module CCNA, l’IT Essentials ou le CCNA Security comprend un certain nombre de chapitres et de nombreux travaux pratiques permettant aux participants de mettre en oeuvre l’ensemble des notions abordées. Il s’agit des cours à suivre dans le but de passer les certifications.</p>
