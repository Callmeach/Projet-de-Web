	<h1>FORMATION PAR ALTERNANCE</h1>
	<p>Cette autre forme de formation permet aux personnes qui sont déjà dans la vie active de pouvoir continuer à se former pour améliorer leurs compétences et de s’adapter aux nouvelles technologies, pratiques ou méthodes appliqués en entreprise. Aussi, elle permet la reconversion professionnelle.</p>
	<p>L’alternance est un dispositif qui combine les aspects pratiques de la formation au sein de l’entreprise et complète les connaissances dans un centre de formation. Le stagiaire alterne les périodes entreprise/centre de formation selon un calendrier prédéfini. C’est une relation tripartite : le centre de formation, le stagiaire et l’entreprise. C’est un véritable partenariat gagnant – gagnant !</p>
	<p>Toutefois, la formation professionnelle continue est sans doute la forme actuellement la plus connue.</p>
	<p>La licence Multimédia, Technologie Web et Infographie (M-TWI), est la seule formation par alternance dont dispose l’Institut Africain d’Informatique, Représentation du Togo.</p>
