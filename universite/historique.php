	<h1>RESEAU IAI</h1>
	<p>Au lendemain des indépendances, la formation de cadres techniques de haut niveau, adaptés aux besoins socio-économiques des pays apparaissait comme l’une des priorités pour soutenir les actions d’un plan de développement national harmonieux.</p>


	<p>C’est ainsi que les chefs d’Etat de l’Organisation Commune Africaine, Malgache et Mauricienne (OCAM), considérant le développement continu et accéléré de l’informatique dans le monde et la nécéssité de disposer d’un personnel qualifié en nombre suffisant pour faire face à ce développement de l’informatique, ont convenu dans le cadre de leur politique de renforcement de la solidarité africaine de créer une école dénommée Institut Africain d’Informatique (IAI) en vue de former le personnel informatique dont ils ont besoin.</p>
	<p>L’institut Africain d’Informatique est une école supérieure en informatique. La convention portant création de l’institut et les statuts y afférent ont été signés le 29 janvier 1972 à Fort Lamy (actuel Ndjaména) en république du TCHAD. L’accord de siège entre l’IAI et le GABON a été signé en janvier 1975. Il est par conséquent un établissement inter-Etats.</p>

	<p>Dans l’optique d’approcher l’institut des pays membres et pour des soucis de performance toujours plus accrue, le conseil d’administration de l’IAI a autorsisé la délocalisation du premier cycle de l’institut dans les pays membres. C’est ainsi que l’IAI-Cameroun a été créé en 1999, l’IAI-Niger en 2001, et l’IAI-Togo en 2002.</p>
	
